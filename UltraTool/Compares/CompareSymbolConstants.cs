namespace UltraTool.Compares;

/// <summary>
/// 比较符号常量
/// </summary>
public static class CompareSymbolConstants
{
    /// <summary>
    /// 小于
    /// </summary>
    public const string Less = "<";

    /// <summary>
    /// 大于
    /// </summary>
    public const string Greater = ">";

    /// <summary>
    /// 等于
