    public new const string Equals = "=";

    /// <summary>
    /// 小于等于
    /// </summary>
    public const string LessEquals = "<=";

    /// <summary>
    /// 大于等于
    /// </summary>
    public const string GreaterEquals = ">=";

    /// <summary>
    /// 不等于
    /// </summary>
    public const string NotEquals = "!=";
}